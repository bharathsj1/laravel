    public function getLoggedUserWallet( )
    {
        $user = Auth::user();

        $wallet = Wallet::where('user_id', $user->id)->get()->first();
        if ($wallet) {
            return response()->json([
                'success' => true,
                'data' => $wallet,
                'message' => 'Logged user Wallet',
            ]);
        } else {
            return response()->json([
                'success' => true,
                'data' => [],
                'message' => 'Wallet Not Found',
            ]);
        }
    }
